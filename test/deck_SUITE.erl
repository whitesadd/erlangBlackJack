%%--------------------------------------------------------------------
%% Function: groups() -> [Group]
%% Group = {GroupName,Properties,GroupsAndTestCases}
%% GroupName = atom()
%% Properties = [parallel | sequence | Shuffle | {RepeatType,N}]
%% GroupsAndTestCases = [Group | {group,GroupName} | TestCase]
%% TestCase = atom()
%% Shuffle = shuffle | {shuffle,{integer(),integer(),integer()}}
%% RepeatType = repeat | repeat_until_all_ok | repeat_until_all_fail |
%%              repeat_until_any_ok | repeat_until_any_fail
%% N = integer() | forever
%%--------------------------------------------------------------------
groups() ->
    [].

%%--------------------------------------------------------------------
%% Function: all() -> GroupsAndTestCases | {skip,Reason}
%% GroupsAndTestCases = [{group,GroupName} | TestCase]
%% GroupName = atom()
%% TestCase = atom()
%% Reason = term()
%%--------------------------------------------------------------------
all() ->
    [tc001_init_stop,
     tc002_count,
     tc003_draw].

%%--------------------------------------------------------------------
%% Function: TestCase() -> Info
%% Info = [tuple()]
%%--------------------------------------------------------------------
tc001_init_stop() ->
    [].

%%--------------------------------------------------------------------
%% Function: TestCase(Config0) ->
%%               ok | exit() | {skip,Reason} | {comment,Comment} |
%%               {save_config,Config1} | {skip_and_save,Reason,Config1}
%% Config0 = Config1 = [tuple()]
%% Reason = term()
%% Comment = term()
%%--------------------------------------------------------------------
tc001_init_stop(_Config) ->
    ok = deck:stop(deck:init()).

tc002_count(_Config) ->
    ?NO_OF_CARDS = deck:count(deck:init()),
    ok.

tc003_draw(_Config) ->
    Pid = deck:init(),
    Count = deck:count(Pid),
    Count = length(draw_all_cards(Pid)),
    ok.

%%--------------------------------------------------------------------
%% Auxiliary Functions
%%--------------------------------------------------------------------
draw_all_cards(Pid) ->
    draw_all_cards(Pid, []).

draw_all_cards(Pid, DrawnCardList) ->
    case deck:draw(Pid) of
        empty ->
            lists:reverse(DrawnCardList);
        Card ->
            draw_all_cards(Pid, [Card | DrawnCardList])
    end.
